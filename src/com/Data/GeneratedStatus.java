package com.Data;

public class GeneratedStatus {
	private String userId;


	private String gameWin;

	
	private String gameLost;

	private String gameDraw;

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getGameWin() {
		return gameWin;
	}

	public void setGameWin(String gameWin) {
		this.gameWin = gameWin;
	}

	public String getGameLost() {
		return gameLost;
	}

	public void setGameLost(String gameLost) {
		this.gameLost = gameLost;
	}

	public String getGameDraw() {
		return gameDraw;
	}

	public void setGameDraw(String gameDraw) {
		this.gameDraw = gameDraw;
	}
}
